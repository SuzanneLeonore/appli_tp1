CREATE TABLE IF NOT EXISTS artistes (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    nom TEXT NOT NULL,
    photo TEXT,
    date_naissance TEXT,
    date_mort TEXT,
    style_art TEXT
);

CREATE TABLE IF NOT EXISTS musees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    logo TEXT,
    nom TEXT NOT NULL,
    date_creation TEXT,
    adresse TEXT
);

CREATE TABLE IF NOT EXISTS oeuvres (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    nom TEXT NOT NULL,
    photo TEXT,
    date_creation TEXT,
    nom_auteur TEXT,
    nom_musee TEXT
);


